// Computes metric-matched local() fallback @font-face declarations and writes
// src/styles/font-fallbacks.css. These are the CLS guard for async-loaded
// fonts.css: they give the system fallback font the same ascent/descent/
// line-gap as the real webfont (read straight from the woff2 metrics tables),
// so the line box doesn't change size when the real font swaps in.
// Re-run with `npm run fonts:fallbacks` if a base weight file changes.

import * as fontkit from 'fontkit';
import fs from 'node:fs';

function overridesFor(path) {
  const font = fontkit.openSync(path);
  const upm = font.unitsPerEm;
  return {
    ascent: ((font.ascent / upm) * 100).toFixed(1) + '%',
    descent: ((Math.abs(font.descent) / upm) * 100).toFixed(1) + '%',
    lineGap: ((font.lineGap / upm) * 100).toFixed(1) + '%',
  };
}

const faces = [
  {
    fallbackFamily: 'DM Sans Fallback',
    sourcePath: 'public/fonts/dm-sans-400.woff2',
    localSrc: "local('Helvetica Neue'), local('Helvetica'), local('Arial')",
  },
  {
    fallbackFamily: 'EB Garamond Fallback',
    sourcePath: 'public/fonts/eb-garamond-400.woff2',
    localSrc: "local('Georgia'), local('Times New Roman')",
  },
  {
    fallbackFamily: 'Noto Sans KR Fallback',
    sourcePath: 'public/fonts/notosanskr-400-0.woff2',
    localSrc: "local('Apple SD Gothic Neo'), local('Malgun Gothic'), local('NanumGothic')",
  },
  {
    fallbackFamily: 'Noto Serif KR Fallback',
    sourcePath: 'public/fonts/notoserifkr-400-0.woff2',
    localSrc: "local('Apple SD Gothic Neo'), local('Batang'), local('NanumMyeongjo')",
  },
];

let out = `/* Generated by scripts/compute-font-fallbacks.mjs — do not edit by hand.
   Metric-matched local() fallbacks so the layout box (ascent/descent/line-gap)
   is identical whether text is rendered in the system fallback or the real
   webfont once it loads async — this is what keeps CLS at 0 while fonts.css
   loads non-blocking. Values are derived from the real woff2 metrics vs the
   local system font metrics, not guessed. */\n`;

for (const f of faces) {
  const o = overridesFor(f.sourcePath);
  out += `@font-face {
  font-family: '${f.fallbackFamily}';
  src: ${f.localSrc};
  ascent-override: ${o.ascent};
  descent-override: ${o.descent};
  line-gap-override: ${o.lineGap};
}
`;
  console.log(`✓ ${f.fallbackFamily}: ascent ${o.ascent}, descent ${o.descent}, line-gap ${o.lineGap}`);
}

fs.writeFileSync('src/styles/font-fallbacks.css', out);
console.log('✓ wrote src/styles/font-fallbacks.css');
